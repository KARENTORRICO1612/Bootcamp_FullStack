

// Formas de declarar variables


console.log('Formas de declarar variables');

// Primera forma utilizando var 
var usuario = 'Henry';
var usuario = 'Jose';
usuario = 'Jose Mesa';
console.log(usuario);


// Segunda forma utlizando let

let colorFavorito = 'verde';
colorFavorito = 'purpura';
console.log(colorFavorito);


// La principal entre var y let es el alance o 'SCOPE'
// var tiene un alcance mayor basado en funcion
// let tiene un alcance menor basado solo bloque 


// Forma de declarar const
const emailAdmin = 'hola@dojopy.com';

console.log(emailAdmin);
